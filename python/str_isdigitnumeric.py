from __future__ import print_function

def main():
    str1 = "thebest2006"
    str2 = "'15915017501"

    print("str1: " + str1)
    print("str2: " + str2)

    print("str1.isdigit(): ", str1.isdigit())
    print("str2.isdigit(): ", str2.isdigit())
 
    print("\nstr1.isnumeric(): ", str1.isnumeric())
    print("str2.isnumeric(): ", str2.isnumeric())

if __name__ == "__main__":
    main()
